package com.xt.entity;

public class Session {
    private Integer id;

    private Integer sessionNum;

    private String valuebs;

    private String getWay;

    private String detail;

    private Integer jizuNum;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSessionNum() {
        return sessionNum;
    }

    public void setSessionNum(Integer sessionNum) {
        this.sessionNum = sessionNum;
    }

    public String getValuebs() {
        return valuebs;
    }

    public void setValuebs(String valuebs) {
        this.valuebs = valuebs;
    }

    public String getGetWay() {
        return getWay;
    }

    public void setGetWay(String getWay) {
        this.getWay = getWay;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public Integer getJizuNum() {
        return jizuNum;
    }

    public void setJizuNum(Integer jizuNum) {
        this.jizuNum = jizuNum;
    }
}